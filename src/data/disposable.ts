/**
 * Check if a domain is a known disposable email provider.
 *
 * @param domain - The domain to check (case-insensitive)
 * @returns true if disposable
 */
export function isDisposableDomain(domain: string): boolean {
  return DISPOSABLE_DOMAINS.has(domain.toLowerCase());
}

/**
 * Get the count of disposable domains in the list.
 */
export function getDisposableDomainsCount(): number {
  return DISPOSABLE_DOMAINS.size;
}
